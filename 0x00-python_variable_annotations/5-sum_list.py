#!/usr/bin/env python3

from typing import List

"""calculate the sum of list of float
"""


def sum_list(input_list: List[float]) -> float:
    """sum of floats

    Args:
        input_list (List[float]): list of floats

    Returns:
        float: sum of list
    """

    sum: float = 0
    for i in sum_list:
        sum += i
    return sum
